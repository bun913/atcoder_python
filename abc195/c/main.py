"""
1以上N以下の整数を1度ずつ書くときコンマは合計で何回書かれるか
コンマが描かれる条件10**3,10**6を超えるタイミングでコンマが1個ずつ増えていく
"""
N = int(input())

ans = 0
if N >= 10 ** 3:
    ans += N - 999
if N >= 10 ** 6:
    ans += N - 999999
if N >= 10 ** 9:
    ans += N - 999999999
if N >= 10 ** 12:
    ans += N - 999999999999
if N >= 10 ** 15:
    ans += N - 999999999999999
print(ans)
